#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*trimed;
	size_t	start;
	size_t	end;
	size_t	i;

	start = 0;
	if (!s1 || !set)
		return (NULL);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	trimed = (char *)malloc(sizeof(char) * (end - start + 1));
	if (!trimed)
		return (NULL);
	i = 0;
	while (start < end)
		trimed[i++] = s1[start++];
	trimed[i] = '\0';
	return (trimed);
}
