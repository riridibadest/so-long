#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	what;

	i = 0;
	what = 0;
	while (src[what] != '\0')
	{
		what++;
	}
	if (size == 0)
		return (what);
	while (i < size - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	if (i < size)
		dest[i] = '\0';
	return (what);
}
